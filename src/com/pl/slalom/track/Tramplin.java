package com.pl.slalom.track;

public class Tramplin
{
	public float position;
	public float left;
	public float right;
	public int power;

	public Tramplin(float position, float left, float right, int power)
	{
		this.position = position;
		this.left = left;
		this.right = right;
		this.power = power;
	}
}
